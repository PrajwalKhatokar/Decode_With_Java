package Basic;
public class UpdationVariable {
    public static void main(String[] args) {
        int x=5;
        System.out.println(x);
        x=7;
        System.out.println(x);
        x=x+6;
        System.out.println(x);
        x=8;
        System.out.println(x);
        x=x-10;
        System.out.println(x);
        //same variable updating each time x=5 when x=7 then 5 wil be replaced and
        //prints the 7
    }
}
